# 题目：一个整数，它加上100后是一个完全平方数，再加上168又是一个完全平方数，请问该数是多少？
"""
首先设置有个未知数x  之后有以下思路
x+100=n**2,x+100+168=m**2
m**2-n**2=168 ,  (m+n)*(m-n)=168  m,n全是偶数
设 m+n=i m-n=j  (i+j)/2=m  , (i-j)/2=n

"""
for i in range(1, 85):
    if 168 % i == 0:
        j = 168 / i
        if i > j and (i + j) % 2 == 0 and (i - j) % 2 == 0:
            m = (i + j) / 2
            n = (i - j) / 2
            x = n ** 2 - 100
            print(x)
